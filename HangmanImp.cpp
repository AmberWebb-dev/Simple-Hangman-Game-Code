#include "Hangman.hpp"
#include <iostream>

using namespace std;

//****************************************************************************************************

char Game::getWord() {
    return Word;
}

//****************************************************************************************************

void Game::setWord(char word) {
    Word = word;
}

//****************************************************************************************************

bool Game::Search(Game *h, char g) {
    Game *cur = h;
    
    while (cur != NULL) {
        if (cur->Word == g){
            return true;
        }
        cur = cur->Next;
    }
    return false;
}

//****************************************************************************************************

void Game::setNext(Game *value){
    Next = value;
}

//****************************************************************************************************

Game* Game::getNext(){
    return Next;
}

//****************************************************************************************************

Game::Game(char word, bool blank, Game *Next) {
    Word = word;
    Blank = false;
    Next = Next;
}

//****************************************************************************************************

Game::Game() {
    Word = ' ';
    Blank = false;
}

